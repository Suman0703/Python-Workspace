# these are generally used for sequential traversal

list = [1,2,3,4,5]
for el in list:
    print(el)


tuple =(9,8,5,6,7)
for num in tuple:
    print(num)

str = "Welcome"
for char in str:
    print(char)


# we can use optional else statement

string = "Welcome Back"

for charr in string:
    print(charr)
else:
    print("Completed")